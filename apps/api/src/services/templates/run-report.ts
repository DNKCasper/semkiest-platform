/**
 * Run report Excel template.
 *
 * Builds a workbook with the following sheets:
 *   1. Summary          — KPI cards, pass/fail overview, chart data table
 *   2. UI & Functional  — UI + functional test results
 *   3. Visual           — Visual regression test results
 *   4. Performance      — Performance test results with timing data
 *   5. Accessibility    — Accessibility test results with severity
 *   6. Security         — Security test results with severity
 *   7. API              — API test results
 */
import ExcelJS from 'exceljs';
import type { TestRunReport, TestResult, TestCategory, ExcelExportOptions } from '../../types/reports';
import { setBorder, formatPercent } from '../excel-export';

// ---------------------------------------------------------------------------
// Colour palette
// ---------------------------------------------------------------------------
const COLORS = {
  titleBg: 'FF1F3864',
  titleFg: 'FFFFFFFF',
  headerBg: 'FF4472C4',
  headerFg: 'FFFFFFFF',
  subheaderBg: 'FFD6E4F7',
  altRowBg: 'FFF2F7FD',
  passed: 'FF70AD47',
  failed: 'FFED7D31',
  skipped: 'FFFFC000',
  error: 'FFE74C3C',
  border: 'FFBDD7EE',
  critical: 'FFED2939',
  high: 'FFFF6B35',
  medium: 'FFFFC300',
  low: 'FF52C41A',
  info: 'FF1890FF',
} as const;

