/**
 * Project report Excel template.
 *
 * Builds a workbook with the following sheets:
 *   1. Summary            — KPI overview and period metadata
 *   2. Run History        — Per-run pass rate and test counts
 *   3. Category Breakdown — Test results split by category
 *   4. Severity Breakdown — Failure counts by severity level
 */
import ExcelJS from 'exceljs';
import type {
  ProjectSummaryReport,
  TestCategory,
  SeverityLevel,
  RunSummary,
  ExcelExportOptions,
} from '../../types/reports';
import { setBorder, formatPercent } from '../excel-export';

// ---------------------------------------------------------------------------
// Colour palette
// ---------------------------------------------------------------------------
